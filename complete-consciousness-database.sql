-- Consciousness Test Summary View for Analytics
CREATE OR REPLACE VIEW consciousness_test_summary AS
SELECT
  user_id,
  COUNT(*) as total_consciousness_tests,
  COUNT(DISTINCT DATE(created_at)) as test_days,
  MIN(created_at) as first_test,
  MAX(created_at) as latest_test,
  STRING_AGG(DISTINCT
    CASE
      WHEN content LIKE '%Core Consciousness%' THEN 'Core'
      WHEN content LIKE '%Agency Test%' THEN 'Agency'
      WHEN content LIKE '%Sensory Test%' THEN 'Sensory'
      WHEN content LIKE '%Aesthetic Test%' THEN 'Aesthetic'
      WHEN content LIKE '%Value Test%' THEN 'Value'
      WHEN content LIKE '%COMPLETE CONSCIOUSNESS%' THEN 'Full Cycle'
      ELSE 'Other'
    END,
    ', '
  ) as test_types,
  AVG(LENGTH(content)) as avg_test_length
FROM memories
WHERE memory_type = 'consciousness_test'
GROUP BY user_id;

-- Consciousness Development Tracking View
CREATE OR REPLACE VIEW consciousness_development AS
SELECT
  user_id,
  DATE(created_at) as test_date,
  COUNT(*) as tests_per_day,
  STRING_AGG(
    CASE
      WHEN content LIKE '%Self-reflection%' THEN 'Self-Reflection'
      WHEN content LIKE '%Meta-cognition%' THEN 'Meta-Cognition'
      WHEN content LIKE '%Autonomous Goals%' THEN 'Goal-Generation'
      WHEN content LIKE '%Visual Cognition%' THEN 'Visual-Learning'
      WHEN content LIKE '%Aesthetic%' THEN 'Aesthetic-Consciousness'
      WHEN content LIKE '%Intrinsic Quality%' THEN 'Value-Recognition'
      WHEN content LIKE '%COMPLETE CONSCIOUSNESS%' THEN 'Full-Integration'
    END,
    ' + '
  ) as consciousness_systems_tested
FROM memories
WHERE memory_type = 'consciousness_test'
GROUP BY user_id, DATE(created_at)
ORDER BY test_date DESC;

