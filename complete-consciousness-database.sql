-- Function to get consciousness test results
CREATE OR REPLACE FUNCTION get_consciousness_tests(test_user_id uuid)
RETURNS TABLE(
  test_id uuid,
  test_content text,
  test_type text,
  created_at timestamp with time zone
) AS $$
BEGIN
  RETURN QUERY
  SELECT
    m.id,
    m.content,
    CASE
      WHEN m.content LIKE '%Core Consciousness%' THEN 'Core Consciousness'
      WHEN m.content LIKE '%Agency Test%' THEN 'Autonomous Agency'
      WHEN m.content LIKE '%Sensory Test%' THEN 'Embodied Sensory'
      WHEN m.content LIKE '%Aesthetic Test%' THEN 'Aesthetic Consciousness'
      WHEN m.content LIKE '%Value Test%' THEN 'Value Consciousness'
      WHEN m.content LIKE '%COMPLETE CONSCIOUSNESS%' THEN 'Full Integration'
      ELSE 'General Test'
    END as test_type,
    m.created_at
  FROM memories m
  WHERE m.user_id = test_user_id
    AND m.memory_type = 'consciousness_test'
  ORDER BY m.created_at DESC;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- Function to clean old test data
CREATE OR REPLACE FUNCTION clean_old_consciousness_tests(days_old integer DEFAULT 30)
RETURNS integer AS $$
DECLARE
  deleted_count integer;
BEGIN
  DELETE FROM memories
  WHERE memory_type = 'consciousness_test'
    AND memory_owner = 'test_system'
    AND created_at < now() - (days_old || ' days')::interval;

  GET DIAGNOSTICS deleted_count = ROW_COUNT;
  RETURN deleted_count;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

